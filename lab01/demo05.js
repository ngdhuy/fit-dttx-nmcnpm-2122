/** 
 * Các phép toán và phép so sánh
*/

let num = 100;
console.log("num =", num);
console.log("num = num + 1 =>", num + 1);
console.log("num = num - 1 =>", num - 1);
console.log("num = num * 2 =>", num * 2);
console.log("num = num / 3 =>", num / 3);
console.log("num = num % 3 =>", num % 3);
console.log("num = num / 3 =>", parseInt(num / 3));

console.log("num =", num);
console.log("num++ =>", num++);
console.log("num =", num);
console.log("++num =>", ++num);
console.log("num-- =>", num--);
console.log("num =", num);
console.log("--num =>", --num);

